package com.Java_Technical;

public class TryCatchExample {

    public static void main(String[] args) {

        int num4=8;
        int num6=9;

        int num9=10;

        int num11=num9/0;

        System.out.println(num11);
        System.out.println(num6);
        System.out.println(num9);
        System.out.println(num4);
    }
}
